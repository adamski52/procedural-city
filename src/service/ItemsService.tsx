    public getItemsByTags(tags:TagsEnum[]) {
        if(tags.length <= 0) {
            return this.items;
        }

        return this.items.filter((item) => {
            let itemTags = item.getTags();
            for(let i = 0; i < tags.length; i++) {
                if(itemTags.indexOf(tags[i]) < 0) {
                    return false;
                }
            }

            return true;
        });
    }
